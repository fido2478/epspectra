template<class oType>int
GsmSpeechDecoder<oType>::forecast(VrSampleRange output, VrSampleRange inputs[]) 
{
  /* no history */
  inputs[0].index = (output.index/160)*33; // 33 ---> 160 (evry 20ms)
  inputs[0].size  = (output.size/160)*33;  
#ifdef DEBUG
  printf("%s (forecast): (output) SRange[%lld, %lu] => upstream(input) SRange[%lld, %lu]\n",name(),output.index, output.size, inputs[0].index,inputs[0].size);
#endif
  return 0;
}

template<class oType>int
GsmSpeechDecoder<oType>::downcast(VrSampleRange input, VrSampleRange* output) 
{
  /* no history */
  output->index=(input.index/33)*160; 
  output->size=(input.size/33)*160; 
#ifdef DEBUG
  printf("%s (downcast): input [%lld, %lu] => output [%lld, %lu]\n",name(),input.index,input.size,output->index, output->size);
#endif
  return 0;
}

#define	S2U(x)	(s2u[ ((unsigned short)(x)) >> 3 ])

template<class oType>int
GsmSpeechDecoder<oType>::work(VrSampleRange output, void *ao[],
				VrSampleRange inputs[], void *ai[])
{
  unsigned  char **i = (unsigned char **)ai;
  oType **o = (oType **)ao;

  unsigned int len = inputs[0].size;
  static int inc_gsm = 33;
  static int inc_ulaw = 160;
  int offset_gsm = 0;
  int offset_ulaw = 0;
  gsm_signal out[160];// c'est "short int"
  
  unsigned int nb_bursts = len/33;
  
  // Decode a 260-bit GSM frame into a 20 ms u-law encoded speech
  {
    for(unsigned int k=0; k<nb_bursts; k++) {
	gsm_decode(r_decoder, (gsm_byte *)(i[0] + offset_gsm), (gsm_signal *)out);
#ifdef DEBUG
      // debug ici
	//printf ("inter2 out[%d]:\n",output.index + offset_ulaw);
      //show2((unsigned char*)out);
      // fin debug
      //printf("offset_ulaw: %d \n",offset_ulaw);
#endif
      register oType *pt = o[0] + offset_ulaw;
      {
	for(int j=0; j<160; j++){
	  if(sizeof(oType)==1){    //if output type is char
	    *pt++ = S2U(out[j]); //translate short into char
	  }
	  else{
	    *pt++ =out[j];
	  }
	}
	// debug ici
        //printf ("gsm_decode o[%d]:\n",output.index + offset_ulaw);
        //show2(o[0] + offset_ulaw);
        // fin debug
      }
      offset_gsm += inc_gsm;
      offset_ulaw += inc_ulaw;
    }
  }
  return output.size;
}

